package com.hzhztech.kotlinlearn.varandval

/**
 * 都用var 定义, 如果后面不指定类型的话, koltin会进行变量类型推断
 */
fun main(args: Array<String>) {
    var i  = 18

//    i = 9999999999999 报错
    //显示指定类型
    var j:String = "haha"

    var z:Long = 999999999999999


    //常量
    val number = "No.123456"
//    number = "123"  报错


}